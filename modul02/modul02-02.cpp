#include <cstdio>		

using namespace std;	

int main() {		
    int a=12,b=13;		

    printf("Nilai A	= %d",a);	
    printf("\nNilai ++A = %d",++a);
    printf("\nNilai A	= %d",a);

    printf("\nNilai A++  = %d",a++);
    printf("\nNilai A	= %d",a);
    printf("\n\nNilai B = %d",b);

    printf("\nNilai --B	= %d",--b);
    printf("\nNilai B	= %d",b);
    printf("\nNilai B--	= %d",b--);
    printf("\nNilai B	= %d",b);

    return 0;
}
